if __name__ == "__main__":
    A = [10, -5, 3, -2, -8, 20, -1, 5]
    # Минимум: -8 (индекс 4), Максимум: 20 (индекс 5)
    # Между ними элементов нет, сумма 0.
    
    B = [30, -3, -7, 10, -2, 5, -10, 4]
    # Максимум: 30 (индекс 0), Минимум: -10 (индекс 6)
    # Элементы между: -3, -7, 10, -2, 5. Отрицательные: -3, -7, -2.
    # Ожидаемая сумма: -12.
    
    result = sum_negatives_between_extrema(B)
    print(f"Массив: {B}")
    print(f"Сумма отрицательных между экстремумами: {result}")