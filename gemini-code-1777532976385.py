def sum_negatives_between_extrema(arr):
    """
    Находит сумму отрицательных элементов, расположенных между 
    минимальным и максимальным элементами массива.
    """
    if not arr or len(arr) < 3:
        return 0

    # 1. Находим индексы минимального и максимального элементов
    # Метод index() возвращает индекс первого вхождения [3]
    min_idx = arr.index(min(arr))
    max_idx = arr.index(max(arr))

    # 2. Определяем границы интервала (от меньшего индекса к большему) 
    start = min(min_idx, max_idx)
    end = max(min_idx, max_idx)

    # 3. Суммируем отрицательные элементы строго между ними
    negative_sum = 0
    for i in range(start + 1, end):
        if arr[i] < 0:
            negative_sum += arr[i]
            
    return negative_sum

